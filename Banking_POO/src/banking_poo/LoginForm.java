    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
            try{
            FileReader open = new FileReader("Cadastro.txt");
            BufferedReader openning = new BufferedReader(open);
            ArrayList<Utilizador> list = new ArrayList();
            
            String[] lista;
            String linha = openning.readLine();
            
            while(linha != null){
                 lista = linha.split(";");
                 
                String nome = lista[0];
                int idade = Integer.parseInt(lista[1]);
                String sexo = lista[2];
                String utilizador = lista[3];
                String senha = lista[4];
                String historico = lista[5];
                list.add(new Utilizador( nome, idade, sexo, utilizador, senha, historico));                 
                linha = openning.readLine();
            }        
            
            for(int x = 0; x < list.size(); x++){
                Utilizador validar = list.get(x);

                String utilizador = user.getText();
                String senha = password.getText();
            
        if(user.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "INSIRA O USUARIO");
            break;
            
        }else if(password.getText().isEmpty()){  
            JOptionPane.showMessageDialog(null, "INSIRA A SENHA");   
            break;
                      
            }else if(validar.getUtilizador().equals(utilizador) && validar.getSenha().equals(senha)){
                
                JOptionPane.showMessageDialog(null, "CONCLUIDO");
                
                //Singleton variavel global => Sessoes
                Sessao.setUtilizador(validar.getUtilizador());
                        
                MainFrame tela = new MainFrame(user.getText());
                tela.setVisible(true);
                this.dispose();
                break;
                            
                    }else if (x == list.size() - 1){
                       JOptionPane.showMessageDialog(null, "ERRO, ACESSO NEGADO");
                    break;
                } 
            }
        }catch(Exception ex){
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

